    @api.model
    def _cron_process_documents(self):
        to_check = self.search([('state', 'in', ['Pending', 'Processing', 'Send Success'])])
        if to_check:
            to_check._check_status()

        to_send = self.search([('state', '=', 'ready_to_send')])
