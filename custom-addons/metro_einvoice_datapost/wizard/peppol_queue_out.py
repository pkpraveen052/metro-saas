    def _compute_invoice_response_count(self):
        for rec in self:
            invoice_response_count = self.env['invoice.responses.queue.in'].search_count(
                [('invoice_queue_out_id', '=', rec.id)])
            rec.received_inv_responses_count = invoice_response_count

