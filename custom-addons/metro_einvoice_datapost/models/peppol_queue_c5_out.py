    def _add_to_queue(self, document):
        document.ensure_one()
        entry = self.search([
            ('invoice_id', '=', document.id),
        ])
        if entry:
            return entry
        # Check if Peppol ID is provided on the form or not
        if not document.partner_id.commercial_partner_id.peppol_identifier:
            raise ValidationError(_("You cannot send the record %s to Peppol.\nPeppol Identifier for this Contact %s "
                                    "is not set. If this contact is an individual, please try to set the Peppol ID to his/her company(Parent).") % (document.name, document.partner_id.name))
        if not document.partner_id.commercial_partner_id.country_id:
            raise ValidationError(_("The Country is not tagged to the Partner."))
        # Check if Customer is peppol registered or not
        params = self.env['ir.config_parameter'].sudo()
        peppol_endpoint = params.get_param('peppol_endpoint', default=False)
        token = params.get_param('peppol_apikey', default=False)
        electronic_address_scheme = params.get_param('electronic_address_scheme', default="0195")
        url = "{}private/api/search/exist?pid=iso6523-actorid-upis::{}:{}".format(peppol_endpoint,
                                                                                  electronic_address_scheme,
                                                                                  document.partner_id.commercial_partner_id.peppol_identifier or '')
        headers = {'Content-type': 'application/json;charset=UTF-8', 'Authorization': 'Bearer {}'.format(token)}
        try:
            response = requests.get(url, headers=headers)
            print('\n\n\nresponse', response)
            if response.status_code in (200, 204):
                pass
            elif response.status_code == 404:
                raise ValidationError(_('Customer not registered in Peppol. You cannot send the invoice to Peppol.'))
            elif response.status_code == 400:
                message = 'Bad request'
                raise ValidationError(_('400 - Bad Request'))
            else:
                raise ValidationError(_('API Responded with status code: %s') % str(response.status_code))
        except Exception as e:
            raise ValidationError(_('%s\nSomething went wrong while trying to check the Customer whether registered '
                                    'in Peppol or not.\n%s\n') % (document.name, str(e)))

        return self.create({
            'invoice_type': document.move_type,
            'invoice_id': document.id,
        })

