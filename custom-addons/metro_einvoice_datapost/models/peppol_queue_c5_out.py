    @api.constrains('invoice_id', 'invoice_ids')
    def _check_unique_invoice_ids(self):
        for record in self:
            invoice_ids_list = record.invoice_ids.ids
            existing_records = self.search([
                ('id', '!=', record.id),
                '|',
                ('invoice_id', 'in', invoice_ids_list + [record.invoice_id.id] if record.invoice_id else record.invoice_ids.ids),
                ('invoice_ids', 'in', invoice_ids_list + [record.invoice_id.id] if record.invoice_id else record.invoice_ids.ids)
            ])
            if existing_records:
                raise ValidationError("Can only send a single Document to PEPPOL once.")

