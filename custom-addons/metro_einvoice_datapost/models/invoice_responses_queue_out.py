    def check_clarification_required(self):
        for obj in self:
            if obj.response_code in ['UQ','RE','CA'] and not obj.status_ids:
                raise ValidationError("Clarification is mandatory for the selected status. \nClarification may be given as a code, a description or both. If both are used, they must indicate the same clarification.\nPlease do the required updations.")
            
