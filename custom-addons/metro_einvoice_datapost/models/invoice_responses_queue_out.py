    def generate_application_response_xml(self):
        print("generate_application_response_xml ===",self)
        self.issue_date = datetime.now()
        print(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>Method called",self.issue_date)
        
        nsmap = {
            None: "urn:oasis:names:specification:ubl:schema:xsd:ApplicationResponse-2",
            'cbc': "urn:oasis:names:specification:ubl:schema:xsd:CommonBasicComponents-2",
            'cac': "urn:oasis:names:specification:ubl:schema:xsd:CommonAggregateComponents-2",
            'xsi': "http://www.w3.org/2001/XMLSchema-instance"
          }

        root = etree.Element(
            "{urn:oasis:names:specification:ubl:schema:xsd:ApplicationResponse-2}ApplicationResponse",
            nsmap=nsmap
        )

        ns = {
            "cac": "{urn:oasis:names:specification:ubl:schema:xsd:"
                   "CommonAggregateComponents-2}",
            "cbc": "{urn:oasis:names:specification:ubl:schema:xsd:"
                   "CommonBasicComponents-2}"
       }

        customization_id = etree.Element(ns['cbc'] + "CustomizationID")
        customization_id.text = "urn:fdc:peppol.eu:poacc:trns:invoice_response:3"
        profile_id = etree.Element(ns['cbc'] + "ProfileID")
        profile_id.text = "urn:fdc:peppol.eu:poacc:bis:invoice_response:3"

        document_id = etree.Element(ns['cbc'] + "ID")
        document_id.text = self.name

        issue_date = etree.Element(ns['cbc'] + "IssueDate")
        issue_date.text = str(self.issue_date.date())
        issue_time = etree.Element(ns['cbc'] + "IssueTime")
        issue_time.text = self.issue_date.strftime('%H:%M:%S')

        # Create SenderParty element and its child elements        
        sender_party_id = self.sender_party_id.split(":")[1]
        scheme_id = self.sender_party_id.split(":")[0]

        sender_party = etree.Element(ns['cac'] + "SenderParty")

        endpoint_id = etree.Element(ns['cbc'] + "EndpointID", schemeID=scheme_id)
        endpoint_id.text = self.sender_party_id.split(":")[1]

        party_legal_entity = etree.Element(ns['cac'] + "PartyLegalEntity")
        registration_name = etree.Element(ns['cbc'] + "RegistrationName")
        
        partner_obj = self.env['res.partner'].search(['|',('peppol_identifier','=',sender_party_id), ('peppol_identifier','ilike',sender_party_id)], limit=1)
        if not partner_obj:
            raise ValidationError('Sender Partner in ERP is not found with Peppol Identifier: ' + self.sender_party_id)
        else:
            registration_name.text = partner_obj.name

        if self.sender_party_contact_name:
            contact = etree.Element(ns['cac'] + "Contact")

            name = etree.Element(ns['cbc'] + "Name")
            name.text = self.sender_party_contact_name or ""
            telephone = etree.Element(ns['cbc'] + "Telephone")
            telephone.text = self.sender_party_contact_telephone or ""
            electronic_mail = etree.Element(ns['cbc'] + "ElectronicMail")
            electronic_mail.text = self.sender_party_contact_email or ""

            contact.append(name)
            contact.append(telephone)
            contact.append(electronic_mail)

            

        party_legal_entity.append(registration_name)

        sender_party.append(endpoint_id)
        sender_party.append(party_legal_entity)
        if self.sender_party_contact_name:
            sender_party.append(contact)
        

        # Create ReceiverParty element and its child elements
        receiver_peppol_id = self.receiver_party_id.split(":")[1]
        scheme_id = self.receiver_party_id.split(":")[0]

        receiver_party = etree.Element(ns['cac'] + "ReceiverParty")        

        endpoint_id_receiver = etree.Element(ns['cbc'] + "EndpointID", schemeID=scheme_id)
        endpoint_id_receiver.text = receiver_peppol_id

        party_legal_entity_receiver = etree.Element(ns['cac'] + "PartyLegalEntity")
        registration_name_receiver = etree.Element(ns['cbc'] + "RegistrationName")

        partner_obj = self.env['res.partner'].search(['|',('peppol_identifier','=',receiver_peppol_id), ('peppol_identifier','ilike',receiver_peppol_id)], limit=1)
        if not partner_obj:
            raise ValidationError('Receiver Partner in ERP is not found with Peppol Identifier: ' + self.receiver_party_id)
        else:
            registration_name_receiver.text = partner_obj.name

        party_legal_entity_receiver.append(registration_name_receiver)

        receiver_party.append(endpoint_id_receiver)
        receiver_party.append(party_legal_entity_receiver)

        # ----- Document response
        document_response = etree.Element(ns['cac'] + "DocumentResponse")

        response = etree.Element(ns['cac'] + "Response")

        response_code = etree.Element(ns['cbc'] + "ResponseCode", listID="UNCL4343OpSubset")
        response_code.text = self.response_code

        effective_date = etree.Element(ns['cbc'] + "EffectiveDate")
        effective_date.text = str(self.effective_date)

        response.append(response_code)
        response.append(effective_date)

        for status_line in self.status_ids:
            status = etree.Element(ns['cac'] + "Status")

            if status_line.clarification_code_id:
                status_reason_code = etree.Element(ns['cbc'] + "StatusReasonCode", listID=status_line.clarification_code_id.status_type)
                status_reason_code.text = status_line.clarification_code_id.code

                status.append(status_reason_code)

            if status_line.clarification_description:
                status_reason = etree.Element(ns['cbc'] + "StatusReason")
                status_reason.text = status_line.clarification_description

                status.append(status_reason)        
            
            if status_line.condition_code_ids:
                for condition_line in status_line.condition_code_ids:
                    condition = etree.Element(ns['cac'] + "Condition")

                    if condition_line.condition_code:
                        condition_code = etree.Element(ns['cbc'] + "AttributeID")
                        condition_code.text = condition_line.condition_code

                        condition.append(condition_code)

                    if condition_line.condition_value:
                        condition_value = etree.Element(ns['cbc'] + "Description")
                        condition_value.text = condition_line.condition_value

                        condition.append(condition_value)

                    status.append(condition)

        if self.status_ids:
            response.append(status)

        document_response.append(response)

        #----- <cac:DocumentResponse>
        document_reference = etree.Element(ns['cac'] + "DocumentReference")

        document_id_ref = etree.Element(ns['cbc'] + "ID")
        document_id_ref.text = self.source_doc_id.invoice_no

        issue_date_ref = etree.Element(ns['cbc'] + "IssueDate")
        issue_date_ref.text = str(self.inv_issue_date)

        document_type_code = etree.Element(ns['cbc'] + "DocumentTypeCode")
        document_type_code.text = self.doc_type_code

        document_reference.append(document_id_ref) # <cac:DocumentResponse> -> <cac:DocumentReference> -> <cbc:ID>
        document_reference.append(issue_date_ref) # <cac:DocumentResponse> -> <cac:DocumentReference> -> <cbc:IssueDate>
        document_reference.append(document_type_code) # <cac:DocumentResponse> -> <cac:DocumentReference> -> <cbc:DocumentTypeCode>

        document_response.append(document_reference) # <cac:DocumentResponse>

        # Append all elements to the root
        root.append(customization_id)
        root.append(profile_id)
        root.append(document_id)
        root.append(issue_date)
        root.append(issue_time)
        if self.note:
            note = etree.Element(ns['cbc'] + "Note")        
            note.text = str(self.note)
            root.append(note)
        root.append(sender_party)
        root.append(receiver_party)
        root.append(document_response)
        
        xml_tree = etree.ElementTree(root)

        xml_string = etree.tostring(
            xml_tree, pretty_print=True, encoding="utf-8", xml_declaration=True
        )
        print("xml_string ===",xml_string)
        print("xml_string.decode('utf-8') == ",xml_string.decode('utf-8'))

        # with open("application_response.xml", "wb") as xml_file:
        #     xml_file.write(xml_string)

        return xml_string
        
    def _send(self):
        print("_send ===")
        for obj in self:
            obj.check_clarification_required()            
            xml_string = ''
            access_point = self.env['peppol.access.point.sg'].sudo().search([('company_id','=',obj.company_id.id)], order="create_date")
            if not access_point:
                obj.write({'state': 'error'})
                log = _("Access Point configuration is not defined.")
                obj.message_post(body=log)
                continue
            try:
                xml_string = obj.generate_application_response_xml()
                base_url = access_point.endpoint
                api_version = access_point.api_version
                base64_string = access_point.get_basic_auth_token()
                client_ref = access_point.get_client_ref()
                timeout = self.env['ir.config_parameter'].sudo().get_param('api_timeout', default='20')
                # headers = {
                #     "Accept": "*/*",
                #     "Authorization": "Basic {}".format(base64_string),
                # }
                headers = {
                    "Accept": "*/*",
                    "Authorization": f"Bearer {access_point.access_token}",
                }
                files = {
                    'document': (obj.inv_no + '.xml', xml_string.decode('utf-8'), 'application/xml')
                }
                try:
                    response = requests.post(
                        '%s/api/business/%s/invoice-responses/peppol-invoice-response-2/%s' % (base_url, api_version, client_ref),
                        files=files,
                        headers=headers,
                        timeout=int(timeout))
                except requests.exceptions.Timeout:
                    obj.write({'state': 'error', 'xml_data': xml_string})
                    log = _(
                        '<b>An Exception has arised.</b><br/>A timeout occured while trying to perform the Send '
                        'Documents API.')
                    obj.message_post(body=log)
                    continue
                except requests.exceptions.HTTPError:
                    obj.write({'state': 'error', 'xml_data': xml_string})
                    log = _(
                        '<b>An Exception has arised.</b><br/>HTTP error occurred while trying to perform the Send '
                        'Documents API.')
                    obj.message_post(body=log)
                    continue
                except Exception as e:
                    obj.write({'state': 'error', 'xml_data': xml_string})
                    log = _(
                        '<b>An Exception has arised.</b><br/>The Send Documents API is not reachable, please try '
                        'again later.<br/> '
                        'Exception handler details: %s') % (
                              repr(e))
                    obj.message_post(body=log)
                    continue
                status_code = response.status_code
                response_data = json.loads(response.text)
                print("type(response_data) ===",type(response_data))
                print("status_code ===",status_code)
                print("response_data ===",response_data)
                print("isinstance(response_data, dict) ===",isinstance(response_data, dict))
                if status_code == 200 and isinstance(response_data, dict):
                    obj.write({'state': response_data['info'][0].get('status'),
                               'client_ref': client_ref,
                               'server_receipt': response_data.get('serverReceipt'),
                               'status_message': response_data['info'][0].get('statusMessage'),
                               'upload_time': response_data['info'][0].get('uploadTime'),
                               'extracted_senderid': response_data['info'][0].get('sender'),
                               'extracted_receiverid': response_data['info'][0].get('receiver'),
                               'extracted_docno': response_data['info'][0].get('documentNo'),
                               'invalidated': 'yes' if response_data['info'][0].get('invalidated') == True else 'no',
                               'sentapi_json_response': response_data,
                               'sent': True,
                               'xml_data': xml_string})
                else:
                    obj.write({'state': 'error', 'xml_data': xml_string})
                    log = _("<b>Send Documents API responded as {} {}</b><br/>{}".format(status_code,
                                                                                         ERROR_RESPONSES_DIC.get(
                                                                                             status_code, ''),
                                                                                         response_data))
                    obj.message_post(body=log)
            except Exception as e:
                obj.write({'state': 'error', 'xml_data': xml_string})
                log = _('<b>A Critical Error has arised.</b><br/>Traceback::%s<br/><br/>%s') % (traceback.format_exc(), repr(e))
                obj.message_post(body=log)
                
                
    def action_send(self):
        print("action_send ===",self)
        if self.filtered(lambda q: q.state != 'ready_to_send'):
            raise ValidationError(_("Please select only 'Ready to Send' queue entries to perform this action."))
        self._send()

    def action_check_status(self):
        if self.filtered(lambda q: q.state not in 'Pending,Processing,Send Success'.split(',')):
            raise ValidationError(
                _("Please select only either of the following states queue entries for this action to perform.\n "
                  "'Pending, Processing and Send Success'. "))
        self._check_status()
        
    def action_retry(self):
        if self.filtered(lambda q: q.state not in 'error,Send Error'.split(',')):
            raise ValidationError(
                _("Please select only 'Error', 'Send Error' & 'Cancelled' queue entries to perform this action."))
        # check_status_api = self.filtered(lambda q: q.sent and q.state in ['error'])
        # check_status_api._check_status()

        resend_doc_api = self.filtered(lambda q: q.state in ('Send Error', 'error'))
        resend_doc_api._send()

    def action_reset(self):
        if self.filtered(lambda q: q.state not in 'error,cancelled'.split(',')):
            raise ValidationError(_("Please select only 'Error' or 'Cancelled' queue entries to perform this action."))
        self.write({'state': 'ready_to_send'})
        
    def action_cancel(self):
        if self.filtered(lambda q: q.state not in ('ready_to_send', 'error', 'Send Error')):
            raise ValidationError(
                _("Please select only 'Ready to Send' or 'Error' queue entries to perform this action."))
        self.write({'state': 'cancelled'})
        
